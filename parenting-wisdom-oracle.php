<script>
const tips = <?= $tips_json ?>;
const DAILY_INDEX = <?= $day_of_year % $total ?>;
let currentIndex = DAILY_INDEX;
let saved = JSON.parse(localStorage.getItem('pwo_saved') || '[]');

function renderCard(index, animate) {
    const tip = tips[index];
    const card = document.getElementById('mainCard');

    if (animate) {
        card.classList.add('flipping');
        setTimeout(() => {
            applyCard(tip, index);
            card.classList.remove('flipping');
        }, 200);
    } else {
        applyCard(tip, index);
    }
}

function applyCard(tip, index) {
    const card = document.getElementById('mainCard');
    card.style.setProperty('--grad-start', tip.color_start);
    card.style.setProperty('--grad-end', tip.color_end);

    document.getElementById('cardIcon').textContent = tip.icon;
    document.getElementById('cardExpert').textContent = tip.expert;
    document.getElementById('cardQuote').textContent = tip.quote;
    document.getElementById('cardTip').textContent = tip.tip;
    document.getElementById('cardReflection').textContent = tip.reflection;
    document.getElementById('reflectionBox').style.borderColor = tip.color_end;

    // Update save button state
    const isSaved = saved.some(s => s.index === index);
    const btn = document.getElementById('saveBtn');
    btn.textContent = isSaved ? '✅ Saved' : '🔖 Save';
    btn.className = 'btn-save' + (isSaved ? ' saved' : '');

    // Update dots
    renderDots(index);
}

function shuffle() {
    let next = Math.floor(Math.random() * tips.length);
    while (next === currentIndex && tips.length > 1) {
        next = Math.floor(Math.random() * tips.length);
    }
    currentIndex = next;
    renderCard(currentIndex, true);
}

function toggleSave() {
    const tip = tips[currentIndex];
    const existingIdx = saved.findIndex(s => s.index === currentIndex);
    if (existingIdx >= 0) {
        saved.splice(existingIdx, 1);
    } else {
        saved.push({ index: currentIndex, expert: tip.expert, quote: tip.quote, tip: tip.tip });
    }
    localStorage.setItem('pwo_saved', JSON.stringify(saved));
    renderSaved();
    applyCard(tip, currentIndex);
}

function renderSaved() {
    document.getElementById('savedCount').textContent = saved.length;
    const list = document.getElementById('savedList');
    if (saved.length === 0) {
        list.innerHTML = '<p style="color:#888;font-size:0.85em;text-align:center;padding:12px">No saved tips yet.</p>';
        return;
    }
    list.innerHTML = saved.map((s, i) => `
        <div class="saved-item">
            <button class="remove-saved" onclick="removeSaved(${i})" title="Remove">✕</button>
            <div class="saved-item-expert">${escHtml(s.expert)}</div>
            <div class="saved-item-quote">"${escHtml(s.quote)}"</div>
            <div class="saved-item-tip">${escHtml(s.tip)}</div>
        </div>
    `).join('');
}

function removeSaved(i) {
    saved.splice(i, 1);
    localStorage.setItem('pwo_saved', JSON.stringify(saved));
    renderSaved();
    applyCard(tips[currentIndex], currentIndex);
}

function toggleSavedList() {
    const list = document.getElementById('savedList');
    list.classList.toggle('open');
    renderSaved();
}

function escHtml(str) {
    return str.replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;').replace(/"/g,'&quot;');
}

// Progress dots — show a small sample, highlight current
const DOT_COUNT = 8;
function renderDots(active) {
    const container = document.getElementById('progressDots');
    // Evenly spaced tip indices for dots
    const dotIndices = Array.from({length: DOT_COUNT}, (_, i) => Math.round(i * (tips.length - 1) / (DOT_COUNT - 1)));
    container.innerHTML = dotIndices.map(idx => {
        const col = tips[idx].color_end;
        const isActive = idx === active;
        return `<div class="dot ${isActive ? 'active' : ''}" 
                     style="${isActive ? 'background:' + col : ''}"
                     onclick="jumpTo(${idx})" 
                     title="Tip ${idx + 1}: ${escHtml(tips[idx].expert)}"></div>`;
    }).join('');
}

function jumpTo(index) {
    currentIndex = index;
    renderCard(index, true);
}

// Init
renderDots(currentIndex);
renderSaved();
</script>

